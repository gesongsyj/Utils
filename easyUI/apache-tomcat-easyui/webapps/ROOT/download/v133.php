<ul>
	<li>datagrid: Add 'scrollTo' and 'highlightRow' methods.</li>
	<li>treegrid: Enable treegrid to parse data from <tbody> element.</li>
	<li>combo: Add 'selectOnNavigation' and 'readonly' options.</li>
	<li>combobox: Add 'loadFilter' option to allow users to change data format before loading into combobox.</li>
	<li>tree: Add 'onBeforeDrop' callback event.</li>
	<li>validatebox: Dependent on tooltip plugin now, add 'deltaX' property.</li>
	<li>numberbox: The 'filter' options can be used to determine if the key pressed was accepted.</li>
	<li>linkbutton: The group button is available.</li>
	<li>layout: The 'minWidth','maxWidth','minHeight','maxHeight' and 'collapsible' properties are available for region panel.</li>
</ul>
